# ***** Numbers

# Integer

print(123+456)

# can use _ to space out numbers like using commas
num = 123_456_789
print(num)

# Float
pie = 3.14159

# Mathmatical Operations
print(3 + 5)
print(10 - 5)
print(3 * 5)

# Division always returns a float
print(10 / 5)

# Floor division divides and then rounds down returning an int
print(8//3)

# Exponent
print(2 ** 3)

# Modulo returns the remainder after division
print(10%2)

# PEMDAS is the priority of operations
print(3 * 3 + 3 / 3 - 3)

# Use round() to round numbers, can specify number of digits
print(round(8/3, 2))
